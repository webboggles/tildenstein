def get_wall(x, y):
    ix, iy = int(x), int(y)
    if ix < 0 or ix >= MAP_W or iy < 0 or iy >= MAP_H:
        return 1
    return MAP_DATA[iy * MAP_W + ix]


def get_ground(x, y):
    ix, iy = int(x), int(y)
    if ix < 0 or ix >= MAP_W or iy < 0 or iy >= MAP_H:
        return 0
    return GROUND_DATA[iy * MAP_W + ix]


def is_solid(x, y):
    return get_wall(x, y) != 0


def _blocked(x, y, r):
    for ox in (-r, r):
        for oy in (-r, r):
            if is_solid(x + ox, y + oy):
                return True
    return False


def try_move(px, py, nx, ny):
    r = PLAYER_R
    if not _blocked(nx, ny, r):
        return nx, ny
    if not _blocked(nx, py, r):
        return nx, py
    if not _blocked(px, ny, r):
        return px, ny
    return px, py


